DROP TABLE IF EXISTS JOKES;
CREATE TABLE  IF NOT EXISTS JOKES (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 QUESTION TEXT NOT NULL,
 ANSWER TEXT NOT NULL,
 USED INTEGER NOT NULL
);

DROP TABLE IF EXISTS FACES;
CREATE TABLE IF NOT EXISTS FACES (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 NAME TEXT NOT NULL,
 FAMILY TEXT NOT NULL,
 FILENAME TEXT NOT NULL
);

DROP TABLE IF EXISTS ENCODING;
CREATE TABLE IF NOT EXISTS ENCODING(
ID INTEGER PRIMARY KEY AUTOINCREMENT,
VECTOR BLOB,
ID_FACE INTEGER NOT NULL,
FOREIGN KEY(ID_FACE) REFERENCES FACES(ID)
);

DROP TABLE IF EXISTS TWEETS;
CREATE TABLE  IF NOT EXISTS TWEETS (
 ID INTEGER PRIMARY KEY,
 CONTENT TEXT NOT NULL,
 DATED DATE NOT NULL,
 USER TEXT NOT NULL,
 NAME TEXT NOT NULL
);

DROP TABLE IF EXISTS TWEETS_CONFIG;
CREATE TABLE  IF NOT EXISTS TWEETS_CONFIG (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 ACCOUNT TEXT NOT NULL,
 HASHTAG TEXT NOT NULL,
 USED INTEGER NOT NULL,
 ADS INTEGER NOT NULL
);

DROP TABLE IF EXISTS USER_SENT_SMS;
CREATE TABLE IF NOT EXISTS USER_SENT_SMS (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 ID_FACE INTEGER NOT NULL,
 SENT_DATE TEXT NOT NULL,
 SENT_TIME TEXT NOT NULL,
 FOREIGN KEY(ID_FACE) REFERENCES FACES(ID)
);

DROP TABLE IF EXISTS ADVERTISEMENT;
CREATE TABLE IF NOT EXISTS ADVERTISEMENT (
 ID INTEGER PRIMARY KEY,
 FILENAME TEXT NOT NULL
);

DROP TABLE IF EXISTS SMS_OPTIONS;
CREATE TABLE IF NOT EXISTS SMS_OPTIONS (
 ID INTEGER PRIMARY KEY,
 OPTIONS TEXT NOT NULL
);

DROP TABLE IF EXISTS DIALOGUES;
CREATE TABLE IF NOT EXISTS DIALOGUES (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 SITUATION TEXT NOT NULL,
 PHRASE TEXT NOT NULL
);

DROP TABLE IF EXISTS PHONES;
CREATE TABLE  IF NOT EXISTS PHONES (
 ID INTEGER PRIMARY KEY AUTOINCREMENT,
 EMAIL TEXT NOT NULL,
 PHONE TEXT NOT NULL
);

